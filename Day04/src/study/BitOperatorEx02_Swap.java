package study;

public class BitOperatorEx02_Swap {

	public static void main(String[] args) {
			
		int a = 13, b= 7;
		System.out.println("a & b = " + (a & b));   // 비트의 논리곱 연산자
		System.out.println("a | b = " + (a | b));    	// 비트의 논리합 연산자
		System.out.println("a ^ b = " + (a ^ b)); 	// 비트의 배타적 논리합 연산자
		
	}

}
